using UnityEngine;
using System.Collections;
using System.Collections.Generic;
namespace TangGame{
	public class NPCTalkBean{
		/// <summary>
		/// npcId
		/// </summary>
		public long npcId;
		/// <summary>
		/// npc的功能类型
		/// </summary>
		public NpcFunctionEnum npcType;
		/// <summary>
		/// NPC功能类型集合
		/// </summary>
		public List<int> goods;
		public NPCTalkBean(long npcId,List<int> goods,NpcFunctionEnum npcType){
			this.npcId = npcId;
			this.goods = goods;
			this.npcType = npcType;
		}
	}
}